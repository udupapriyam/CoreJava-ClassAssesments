package com.deloitte;

public class Person {
	int pid;
	String name;
	Passport pp;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Passport getPp() {
		return pp;
	}
	public void setPp(Passport pp) {
		this.pp = pp;
	}
	
	
}
